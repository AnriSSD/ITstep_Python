# Bubble Sort
def bubble_sort(arr):
    n = len(arr)
    for i in range(n):
        for j in range(0, n-i-1):
            if arr[j] > arr[j+1]:
                arr[j], arr[j+1] = arr[j+1], arr[j]

# Merge Sort
def merge_sort(arr):
    if len(arr) > 1:
        mid = len(arr)//2
        L = arr[:mid]
        R = arr[mid:]

        merge_sort(L)
        merge_sort(R)

        i = j = k = 0

        while i < len(L) and j < len(R):
            if L[i] < R[j]:
                arr[k] = L[i]
                i += 1
            else:
                arr[k] = R[j]
                j += 1
            k += 1

        while i < len(L):
            arr[k] = L[i]
            i += 1
            k += 1

        while j < len(R):
            arr[k] = R[j]
            j += 1
            k += 1

# Insertion Sort
def insertion_sort(arr):
    for i in range(1, len(arr)):
        key = arr[i]
        j = i-1
        while j >=0 and key < arr[j] :
                arr[j+1] = arr[j]
                j -= 1
        arr[j+1] = key

# Input list
numbers = [94, 56, 32, 55, 344, 192, 4832, 2, 9, 0, 1]

# Bubble Sort
bubble_sorted = numbers.copy()
bubble_sort(bubble_sorted)
print("Bubble Sort:", bubble_sorted)

# Merge Sort
merge_sorted = numbers.copy()
merge_sort(merge_sorted)
print("Merge Sort:", merge_sorted)

# Insertion Sort
insertion_sorted = numbers.copy()
insertion_sort(insertion_sorted)
print("Insertion Sort:", insertion_sorted)
